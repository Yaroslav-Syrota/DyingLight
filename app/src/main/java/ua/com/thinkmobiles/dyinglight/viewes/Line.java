public class Line {
    int xStart, yStart, xEnd, yEnd;

    public Line() {
    }

    public Line(int _xStart, int _yStart, int _xEnd, int _yEnd) {
        xStart = _xStart;
        yStart = _yStart;
        xEnd = _xEnd;
        yEnd = _yEnd;
    }
}
